#include"3-calc.h"
/**
 * op_add - add
 *
 * @a: input
 * @b: input
 *
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference
 *
 * @a: input
 * @b: input
 *
 * Return: difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product
 *
 * @a: input
 * @b: input
 *
 * Return: mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: input
 * @b: input
 *
 * Return: div
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division
 *
 * @a: input
 * @b: input
 *
 * Return: mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
